    private void opsoma_BActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_opsoma_BActionPerformed
        double valor = Double.parseDouble(visor.getText());
        switch (operacao) {
            case "+":
                result += valor;
                log += log + valor + " ";
                break;
            case "-":
                result -= valor;
                log += log + valor + " ";
                break;
            case "x":
                result *= valor;
                log += log + valor + " ";
                break;
            case "/":
                result /= valor;
                log += log + valor + " ";
                break;
            default:
                result = valor;
                log = log + "" + valor + " ";
                break;
        }
        operacao = "+";
        log += "+ ";
        visor.setText(null);
    }//GEN-LAST:event_opsoma_BActionPerformed

